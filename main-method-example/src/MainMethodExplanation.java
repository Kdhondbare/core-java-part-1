public class MainMethodExplanation {

    /*
    public -> public access - means this method can be accessed from anywhere.
    static -> means this method is not objects method, you can call this method without object creation.
    static methods are basically class level method. they are call through className
    void -> return type - this void means, main method is not returning any value.
    main -> method name
    () -> is required foreach method
    String[] args -> arguments to main - String is data type and String[] is string array
     */

    public static void main(String[] args) {
        System.out.println("Main method executed");

    }
}
